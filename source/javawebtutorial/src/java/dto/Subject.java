package dto;

/**
 *
 * @author Admin
 */
public class Subject {
    private int id;
    
    private String name;
    
    private String state;

    public Subject(int id, String name, String state) {
        this.id = id;
        this.name = name;
        this.state = state;
    }

    public Subject() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }
    
    
}
