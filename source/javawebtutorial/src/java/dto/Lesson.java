package dto;

/**
 *
 * @author Admin
 */
public class Lesson {
    private int id;
    
    private int subjectId;
    
    private String name;
    
    private String state;

    public Lesson(int id, int subjectId, String name, String state) {
        this.id = id;
        this.subjectId = subjectId;
        this.name = name;
        this.state = state;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSubjectId() {
        return subjectId;
    }

    public void setSubjectId(int subjectId) {
        this.subjectId = subjectId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    @Override
    public String toString() {
        return id + "/" + subjectId + "/" + name + "/" + state;
    }
    
    
    
    
}
